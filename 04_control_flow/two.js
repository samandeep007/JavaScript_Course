const month = "april";

switch (month) {
    case 1:
        console.log("January");
        break;

    case 2:
        console.log("february");
        break;

    case 3:
        console.log("march")
        break;

    case "april":
        console.log("april")
        break;

    case 5:
        console.log("may")
        break;

    case 6:
        console.log("june")
        break;
    default:
        console.log("invalid month");
        break;
}